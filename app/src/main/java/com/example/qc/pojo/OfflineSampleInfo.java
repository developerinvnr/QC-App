package com.example.qc.pojo;

public class OfflineSampleInfo {
    String crop;
    String variety;
    String lotno;
    String sampleno;
    String trstage;
    String testtype;
    String seedsize;
    String seedinrep;
    String fgttesttype;
    String noofrep;

    public OfflineSampleInfo() {
    }

    public String getCrop() {
        return crop;
    }

    public void setCrop(String crop) {
        this.crop = crop;
    }

    public String getVariety() {
        return variety;
    }

    public void setVariety(String variety) {
        this.variety = variety;
    }

    public String getLotno() {
        return lotno;
    }

    public void setLotno(String lotno) {
        this.lotno = lotno;
    }

    public String getSampleno() {
        return sampleno;
    }

    public void setSampleno(String sampleno) {
        this.sampleno = sampleno;
    }

    public String getTrstage() {
        return trstage;
    }

    public void setTrstage(String trstage) {
        this.trstage = trstage;
    }

    public String getTesttype() {
        return testtype;
    }

    public void setTesttype(String testtype) {
        this.testtype = testtype;
    }

    public String getSeedsize() {
        return seedsize;
    }

    public void setSeedsize(String seedsize) {
        this.seedsize = seedsize;
    }

    public String getSeedinrep() {
        return seedinrep;
    }

    public void setSeedinrep(String seedinrep) {
        this.seedinrep = seedinrep;
    }

    public String getFgttesttype() {
        return fgttesttype;
    }

    public void setFgttesttype(String fgttesttype) {
        this.fgttesttype = fgttesttype;
    }

    public String getNoofrep() {
        return noofrep;
    }

    public void setNoofrep(String noofrep) {
        this.noofrep = noofrep;
    }
}
