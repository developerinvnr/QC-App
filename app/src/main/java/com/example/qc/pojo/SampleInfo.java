package com.example.qc.pojo;

public class SampleInfo {
    String srdate;
    String crop;
    String variety;
    String lotno;
    String trstage;
    String nob;
    String qty;
    String sloc;
    String qctest;
    String sampleno;
    String spdate;
    String sampflg;
    String massage;
    String srfseries;
    String srfno;

    public SampleInfo() {
    }

    public SampleInfo(String srdate, String crop, String variety, String lotno, String trstage, String nob, String qty, String sloc, String qctest, String sampleno, String spdate, String sampflg, String massage, String srfseries, String srfno) {
        this.srdate = srdate;
        this.crop = crop;
        this.variety = variety;
        this.lotno = lotno;
        this.trstage = trstage;
        this.nob = nob;
        this.qty = qty;
        this.sloc = sloc;
        this.qctest = qctest;
        this.sampleno = sampleno;
        this.spdate = spdate;
        this.sampflg = sampflg;
        this.massage = massage;
        this.srfseries = srfseries;
        this.srfno = srfno;
    }

    public String getSrdate() {
        return srdate;
    }

    public void setSrdate(String srdate) {
        this.srdate = srdate;
    }

    public String getCrop() {
        return crop;
    }

    public void setCrop(String crop) {
        this.crop = crop;
    }

    public String getVariety() {
        return variety;
    }

    public void setVariety(String variety) {
        this.variety = variety;
    }

    public String getLotno() {
        return lotno;
    }

    public void setLotno(String lotno) {
        this.lotno = lotno;
    }

    public String getTrstage() {
        return trstage;
    }

    public void setTrstage(String trstage) {
        this.trstage = trstage;
    }

    public String getNob() {
        return nob;
    }

    public void setNob(String nob) {
        this.nob = nob;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getSloc() {
        return sloc;
    }

    public void setSloc(String sloc) {
        this.sloc = sloc;
    }

    public String getQctest() {
        return qctest;
    }

    public void setQctest(String qctest) {
        this.qctest = qctest;
    }

    public String getSampleno() {
        return sampleno;
    }

    public void setSampleno(String sampleno) {
        this.sampleno = sampleno;
    }

    public String getSpdate() {
        return spdate;
    }

    public void setSpdate(String spdate) {
        this.spdate = spdate;
    }

    public String getSampflg() {
        return sampflg;
    }

    public void setSampflg(String sampflg) {
        this.sampflg = sampflg;
    }

    public String getMassage() {
        return massage;
    }

    public void setMassage(String massage) {
        this.massage = massage;
    }

    public String getSrfseries() {
        return srfseries;
    }

    public void setSrfseries(String srfseries) {
        this.srfseries = srfseries;
    }

    public String getSrfno() {
        return srfno;
    }

    public void setSrfno(String srfno) {
        this.srfno = srfno;
    }
}
